namespace Zicht\Bundle\VersioningBundle\TestAssets;

class OtherEntity
{
    private $id;
    private $name;

    public function __construct($id = null, $name = 'otha!')
    {
        $this->id = $id;
        $this->name = $name;
    }

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param string $name
     */
    public function setName($name)
    {
        $this->name = $name;
    }
}
